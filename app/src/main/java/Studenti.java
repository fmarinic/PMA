public class Studenti {
    public String sIme;
    public String sPrezime;

    public Studenti(String ime, String prezime)
    {
        this.sIme = ime;
        this.sPrezime = prezime;
    }

    public String getsIme() {
        return sIme;
    }

    public String getsPrezime() {
        return sPrezime;
    }
}


